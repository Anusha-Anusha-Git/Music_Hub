    private void refreshMusicTable() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Clear existing rows

    for (Music m : MusicLibrary.getAllMusic()) {
        model.addRow(new Object[]{
            m.getId(),
            m.getTitle(),
            m.getAlbum(),
            m.getArtist(),
            m.getYear(),
            m.getGenre()
        });
    }
}
    
